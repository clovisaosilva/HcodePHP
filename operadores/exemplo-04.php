<?php
	$a = 55.0;
	$b = 55;
	
	//maior
	var_dump($a > $b);
	echo "<br>";
	
	//menor
	var_dump($a < $b);
	echo "<br>";
	
	//comparação de valores
	var_dump($a == $b);
	echo "<br>";
	
	//comparação de valor e tipo
	var_dump($a === $b);
	echo "<br>";
	
	//diferente
	var_dump($a != $b);
	echo "<br>";
	
	//diferente valor e tipo
	var_dump($a !== $b);
	echo "<br>";
?>